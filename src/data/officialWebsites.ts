// Get official website URL for a brand
export const getOfficialWebsite = (brand: string): string | null => {
  return officialBrandWebsites[brand] || null;
};

// Get model-specific page URL (fallback to brand page if not found)
export const getModelPageUrl = (brand: string, model: string): string | null => {
  // First check for exact brand|model match
  const key = `${brand}|${model}`;
  if (modelSpecificUrls[key]) {
    return modelSpecificUrls[key];
  }
  
  // Check for partial model match (e.g., "Corolla" matches "Corolla Makyajlı")
  for (const [urlKey, url] of Object.entries(modelSpecificUrls)) {
    const [keyBrand, keyModel] = urlKey.split('|');
    if (keyBrand === brand && model.toLowerCase().includes(keyModel.toLowerCase())) {
      return url;
    }
  }
  
  // Fallback to brand homepage
  return officialBrandWebsites[brand] || null;
};
