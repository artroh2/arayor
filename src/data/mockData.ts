export const getVehicleById = (id: string): Vehicle | undefined => {
  return vehicles.find((v) => v.id === id);
};

export const getReviewsByVehicleId = (vehicleId: string): Review[] => {
  return reviews.filter((r) => r.vehicleId === vehicleId);
};

export const getVehiclesByBrand = (brandId: string): Vehicle[] => {
  return vehicles.filter((v) => v.brandId === brandId);
};

export const getVehiclesByCategory = (category: string): Vehicle[] => {
  return vehicles.filter((v) => v.category.toLowerCase() === category.toLowerCase());
};

export const getActualReviewCount = (vehicleId: string): number => {
  return reviews.filter((r) => r.vehicleId === vehicleId).length;
};
