// Helper function to get fuel type from specs
export const getFuelType = (vehicle: Vehicle): string => {
  if (vehicle.fuelType) return vehicle.fuelType;
  const fuel = vehicle.specs.fuel.toLowerCase();
  if (fuel.includes("elektrik") && !fuel.includes("benzin")) return "elektrik";
  if (fuel.includes("hibrit") || (fuel.includes("elektrik") && fuel.includes("benzin"))) return "hibrit";
  if (fuel.includes("dizel")) return "dizel";
  if (fuel.includes("lpg")) return "lpg";
  return "benzin";
};

